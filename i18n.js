function getLang() {
  const saved = localStorage.getItem('emotion_lang');
  if (saved && I18N[saved]) return saved;
  const nav = (navigator.language || 'ru').slice(0, 2);
  if (I18N[nav]) return nav;
  return 'ru';
}

function setLang(lang) {
  if (!I18N[lang]) return;
  localStorage.setItem('emotion_lang', lang);
  document.documentElement.lang = lang;
  applyTranslations();
  document.querySelectorAll('[data-lang-btn]').forEach(b => {
    b.classList.toggle('active', b.dataset.langBtn === lang);
  });
  if (typeof onLangChange === 'function') onLangChange(lang);
}

function t(key) {
  const lang = getLang();
  return (I18N[lang] && I18N[lang][key]) || (I18N.ru && I18N.ru[key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
  document.querySelectorAll('[data-lang-btn]').forEach(b => {
    b.addEventListener('click', () => setLang(b.dataset.langBtn));
    b.classList.toggle('active', b.dataset.langBtn === getLang());
  });
  document.documentElement.lang = getLang();
});
